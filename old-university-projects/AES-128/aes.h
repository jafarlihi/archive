void hexOutput(unsigned char input)
{
	if (input / 16 < 10) cout << (char)((input / 16) + '0');
	if (input / 16 >= 10) cout << (char)((input / 16 - 10) + 'A');
	if (input % 16 < 10) cout << (char)((input % 16) + '0');
	if (input % 16 >= 10) cout << (char)((input % 16 - 10) + 'A');
}

