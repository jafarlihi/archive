void addRoundKey(unsigned char* roundKey, unsigned char* currentState)
{
	for (int c = 0; c < 16; c++) { currentState[c] ^= roundKey[c]; }
}

void expandKeyOperations(unsigned char* input, unsigned char rconCounter)
{
	// Rotates 4 byte input to the left
	unsigned char temporary_firstIndex;
	temporary_firstIndex = input[0];
	input[0] = input[1];
	input[1] = input[2];
	input[2] = input[3];
	input[3] = temporary_firstIndex;

	// S-box the values
	input[0] = sbox[input[0]];
	input[1] = sbox[input[1]];
	input[2] = sbox[input[2]];
	input[3] = sbox[input[3]];

	// Rcon step
	input[0] ^= rcon[rconCounter];
}

void expandKey(unsigned char* masterKey, unsigned char* expandedKeys)
{
	for (int c = 0; c < 16; c++) { expandedKeys[c] = masterKey[c]; }

	int byteCounter = 16; // Initialized at 16 because of copied original key
	int rconCounter = 1;
	unsigned char temporary[4];

	while (byteCounter < 176) // 176 bytes of key expansion is expected in normal 128bit AES operation
	{
		for (int c = 0; c < 4; c++) { temporary[c] = expandedKeys[c + byteCounter - 4]; } // Last 4 bytes of the last expansion is used for generation of the next key

		if (byteCounter % 16 == 0) { expandKeyOperations(temporary, rconCounter++); } // Key expansion operations are performed for each new 16 bytes

																					  // XOR operation
		for (unsigned char c = 0; c < 4; c++) {
			expandedKeys[byteCounter] = expandedKeys[byteCounter - 16] ^ temporary[c];
			byteCounter++;
		}
	}
}

void encrypt(unsigned char* masterKey, unsigned char* expandedKey, unsigned char* inputMessage)
{
	unsigned char currentState[16];
	for (int c = 0; c < 16; c++) { currentState[c] = inputMessage[c]; }

	int numberOfRounds = 9; // AES 128bit requires 10 rounds. 9 rounds + 10th final round = 10 rounds.

	addRoundKey(masterKey, currentState);

	for (int c = 0; c < numberOfRounds; c++) {
		subBytes(currentState);
		shiftRows(currentState);
		mixColumns(currentState);
		addRoundKey(expandedKey + (16 * (c + 1)), currentState);
	}

	// 10th round: final round
	subBytes(currentState);
	shiftRows(currentState);
	addRoundKey(expandedKey + 160, currentState);

	for (int c = 0; c < 16; c++) { inputMessage[c] = currentState[c]; }
}